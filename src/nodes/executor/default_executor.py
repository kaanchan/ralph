"""Executor node — writes code to the target repo.

Two code paths based on model type:

LOCAL (ollama/*):
  Call Ollama /api/chat directly. Extract code from ``` block. Write file.
  Local quantized models cannot reliably follow Aider's structured diff
  formats (SEARCH/REPLACE, udiff, whole) — they output whatever their
  training data shaped them to output. Direct API + code extraction is
  the only robust approach for this class of model.

CLOUD (gemini/*, gpt-*, claude-*, etc.):
  Use Aider with --edit-format diff. Cloud-scale models reliably follow
  structured diff formats and Aider's tooling (repo context, multi-file
  edits) adds real value at that scale.

Both paths write to solution.py and test_solution.py separately so the
model generates one file at a time (avoids context-length stalls).
"""
